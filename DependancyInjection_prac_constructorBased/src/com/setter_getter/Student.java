package com.setter_getter;

import java.util.List;

public class Student {
	
	int rollno;
	String name;
	String email;
	List Course;
	
	public Student(int rollno, String name, String email, List course) {
		super();
		this.rollno = rollno;
		this.name = name;
		this.email = email;
		Course = course;
	}
	
	
	

}
